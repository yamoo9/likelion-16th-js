// --------------------------------------------------------------------------
// 연습: 논리 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
true
false
console.log(true)

// --------------------------------------------------------------------------
// 연습: 여러 값 콘솔 패널에 출력
